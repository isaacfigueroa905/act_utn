#include <stdio.h>

/** \brief :Calcula la suma de dos numeros
 *
 * \param int :el primer numero ingresado por el usuario al que se le sumara un segundo numero
 * \param int :el segundo numero ingresado por el usuario que se le sumara al primero
 * \return int :el resultado de sumar ambos numeros
 *
 */

int sumarNumeros(int, int);


/** \brief :Calcula la resta de dos numeros
 *
 * \param int :el primer numero ingresado por el usuario al que se le restara un segundo numero
 * \param int :el segundo numero ingresado por el usuario que se le restara al primero
 * \return int :el resultado de restar ambos numeros
 *
 */
int RestarNumeros(int, int);


/** \brief :Calcula la multiplicacion de dos numeros
 *
 * \param :int el primer numero ingresado por el usuario al que se le multiplicara un segundo numero
 * \param :int el segundo numero ingresado por el usuario por el cual se multiplicara al primero
 * \return :int el resultado de multiplicar ambos numeros
 *
 */
int MultiplicarNumeros(int,int);



/** \brief :calcula la division de dos numeros
 *
 * \param float :el primer numero ingresado por el usuario, que sera el dividendo.
 * \param int :el segundo numero ingresado por el usuario, que sera el divisor.
 * \return int :el cociente de la division.
 *
 */
int DividirNumeros(int,int);




/** \brief :Calcula el factorial de un numero
 *
 * \param int :el numero ingresado por el usuario sobre el cual se calculara el factorial.
 * \return int :el resultado del calcula factorial.
 *
 */
int CalcularFactorial(int);





